#!/usr/bin/env python3

"""
Module for variable declarations.

This module declares and initializes several variables with
different data types.
"""

# Integer variable declaration
a: int = 1
"""
The value of a is an integer representing a whole number.
"""

# Floating point variable declaration
pi: float = 3.14
"""
The value of pi is a floating point number representing the mathematical
constant pi.
"""

# Boolean variable declaration
i_understand_annotations: bool = True
"""
The value of i_understand_annotations is a boolean indicating whether the
user understands type annotations.
"""

# String variable declaration
school: str = "Holberton"
"""
The value of school is a string representing the name of the school.
"""
